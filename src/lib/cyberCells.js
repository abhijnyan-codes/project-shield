/**
 * Retrieves the cyber cell details for a given state.
 * 
 * @param {string} stateName - The name of the state (e.g., "Karnataka").
 * @returns {Object|undefined} The cyber cell data object, or undefined if not found.
 */
export function getCyberCellByState(stateName) {
  if (!stateName) return undefined;
  
  const normalizedSearch = stateName.toLowerCase().trim();
  return cyberCells.find(cell => 
    cell.state.toLowerCase().includes(normalizedSearch)
  );
}

